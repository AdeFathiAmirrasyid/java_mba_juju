package kelas_terbuka;
    public class materi2{
        public static void main(String []args){
            // Operasi  Aritmatika
             
            int a =16;
            int b =2;
            int hasil;
           
            //penjumlahan 
            hasil=a+b;
            System.out.println(a+ " + " +b+ " = " +hasil);
            
            //pengurangan
            hasil=a-b;
            System.out.printf("%d - %d = %d \n",a,b,hasil);
            
            //perkalian
            hasil=a*b;
            System.out.printf("%d x %d = %d \n",a,b,hasil);
            
            //pembagian
            hasil=a/b;
            System.out.printf("%d / %d = %d \n",a,b,hasil);
            
            float d = 11;
            float e = 5;
            float hasile;
            
            hasile = d/e;
            System.out.println(d+" / " +e+ " = "+hasile);
            System.out.printf("%f / %f = %f \n",d,e,hasile);
               
            //modulus
            int c = 12;
            int f = 5;
            int jumlah;
            jumlah=c%f;
            System.out.printf("%d %% %d = %d \n",c,f,jumlah);
            
           
        }
                
    }